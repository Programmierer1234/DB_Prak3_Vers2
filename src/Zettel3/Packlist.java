package Zettel3;

public class Packlist {

    int bstnr, vnnr, vmenge;

    public Packlist(int bstnr, int vbnr, int vmenge) {
        this.bstnr = bstnr;
        this.vnnr = vbnr;
        this.vmenge = vmenge;
    }

    public int getBstnr() {
        return bstnr;
    }

    public void setBstnr(int bstnr) {
        this.bstnr = bstnr;
    }

    public int getVbnr() {
        return vnnr;
    }

    public void setVbnr(int vnnr) {
        this.vnnr = vnnr;
    }

    public int getVmenge() {
        return vmenge;
    }

    public void setVmenge(int vmenge) {
        this.vmenge = vmenge;
    }
}
